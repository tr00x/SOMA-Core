"""Benchmark report — Markdown generator and Rich terminal formatter.

Produces two output modes from BenchmarkResult:
- generate_markdown(): complete Markdown string for docs/BENCHMARK.md
- render_terminal(): rich console output with colored tables
"""

from __future__ import annotations

from soma.benchmark.metrics import BenchmarkResult


# ------------------------------------------------------------------
# Markdown report
# ------------------------------------------------------------------


def _fmt_pct(value: float) -> str:
    """Format a float as a percentage string like '23.4%'."""
    return f"{value * 100:.1f}%"


def _fmt_improvement(value: float) -> str:
    """Format a reduction value as an improvement string like '-23.4%'."""
    if value >= 0:
        return f"-{value * 100:.1f}%"
    return f"+{abs(value) * 100:.1f}%"


def generate_markdown(result: BenchmarkResult) -> str:
    """Return complete Markdown string for docs/BENCHMARK.md."""
    lines: list[str] = []

    lines.append("# SOMA Benchmark Results")
    lines.append("")
    lines.append(f"> Generated: {result.timestamp}")
    lines.append(f"> Runs per scenario: {result.runs_per_scenario}")
    lines.append("")

    # Overall summary
    lines.append("## Summary")
    lines.append("")
    lines.append("| Metric | Without SOMA | With SOMA | Improvement |")
    lines.append("|--------|-------------|-----------|-------------|")

    # Compute aggregate without/with rates from scenario data
    avg_base_err = _avg_baseline_error_rate(result)
    avg_soma_err = _avg_soma_error_rate(result)
    avg_base_retry = _avg_baseline_retry_rate(result)
    avg_soma_retry = _avg_soma_retry_rate(result)
    avg_base_tokens = _avg_baseline_tokens(result)
    avg_soma_tokens = _avg_soma_tokens(result)

    lines.append(
        f"| Errors | {_fmt_pct(avg_base_err)} | {_fmt_pct(avg_soma_err)} "
        f"| {_fmt_improvement(result.overall_error_reduction)} |"
    )
    lines.append(
        f"| Retries | {_fmt_pct(avg_base_retry)} | {_fmt_pct(avg_soma_retry)} "
        f"| {_fmt_improvement(result.overall_retry_reduction)} |"
    )
    lines.append(
        f"| Tokens | {avg_base_tokens:,.0f} | {avg_soma_tokens:,.0f} "
        f"| {_fmt_improvement(result.overall_token_savings)} |"
    )
    lines.append("")

    # Per-scenario sections
    lines.append("## Scenarios")
    lines.append("")

    for scenario in result.scenarios:
        lines.append(f"### {scenario.scenario_name}")
        lines.append("")
        lines.append(f"_{scenario.description}_")
        lines.append("")

        s_err = _scenario_avg(scenario.soma_runs, "error_rate")
        b_err = _scenario_avg(scenario.baseline_runs, "error_rate")
        s_retry = _scenario_avg(scenario.soma_runs, "retry_rate")
        b_retry = _scenario_avg(scenario.baseline_runs, "retry_rate")
        s_tokens = _scenario_avg(scenario.soma_runs, "total_tokens")
        b_tokens = _scenario_avg(scenario.baseline_runs, "total_tokens")

        lines.append("| Metric | Baseline | SOMA | Reduction |")
        lines.append("|--------|----------|------|-----------|")
        lines.append(
            f"| Error Rate | {_fmt_pct(b_err)} | {_fmt_pct(s_err)} "
            f"| {_fmt_improvement(scenario.error_reduction)} |"
        )
        lines.append(
            f"| Retry Rate | {_fmt_pct(b_retry)} | {_fmt_pct(s_retry)} "
            f"| {_fmt_improvement(scenario.retry_reduction)} |"
        )
        lines.append(
            f"| Tokens | {b_tokens:,.0f} | {s_tokens:,.0f} "
            f"| {_fmt_improvement(scenario.token_savings)} |"
        )
        lines.append("")

        # Mode transitions summary
        soma_transitions = sum(
            len(r.mode_transitions) for r in scenario.soma_runs
        )
        baseline_transitions = sum(
            len(r.mode_transitions) for r in scenario.baseline_runs
        )
        lines.append(
            f"Mode transitions: SOMA avg {soma_transitions / max(len(scenario.soma_runs), 1):.1f}, "
            f"baseline avg {baseline_transitions / max(len(scenario.baseline_runs), 1):.1f}"
        )
        lines.append("")

        # TP/FP for SOMA runs
        tp = sum(r.true_positives for r in scenario.soma_runs)
        fp = sum(r.false_positives for r in scenario.soma_runs)
        total_alerts = tp + fp
        if total_alerts > 0:
            precision = tp / total_alerts
            lines.append(
                f"Detection: {tp} true positives, {fp} false positives "
                f"(precision {_fmt_pct(precision)})"
            )
            lines.append("")

    lines.append("---")
    lines.append("")
    lines.append("*Auto-generated by `soma benchmark`. Do not edit manually.*")
    lines.append("")

    return "\n".join(lines)


# ------------------------------------------------------------------
# Rich terminal output
# ------------------------------------------------------------------


def render_terminal(result: BenchmarkResult) -> None:
    """Render benchmark results as rich console output with colored tables."""
    from rich.console import Console
    from rich.panel import Panel
    from rich.table import Table

    console = Console()

    # Overall summary panel
    summary_table = Table(show_header=True, header_style="bold magenta")
    summary_table.add_column("Metric", style="cyan")
    summary_table.add_column("Without SOMA", justify="right")
    summary_table.add_column("With SOMA", justify="right")
    summary_table.add_column("Improvement", justify="right")

    def _color(value: float) -> str:
        """Return colored improvement string."""
        text = _fmt_improvement(value)
        if value > 0.10:
            return f"[green]{text}[/green]"
        elif value > 0:
            return f"[yellow]{text}[/yellow]"
        else:
            return f"[red]{text}[/red]"

    avg_base_err = _avg_baseline_error_rate(result)
    avg_soma_err = _avg_soma_error_rate(result)
    avg_base_retry = _avg_baseline_retry_rate(result)
    avg_soma_retry = _avg_soma_retry_rate(result)
    avg_base_tokens = _avg_baseline_tokens(result)
    avg_soma_tokens = _avg_soma_tokens(result)

    summary_table.add_row(
        "Errors", _fmt_pct(avg_base_err), _fmt_pct(avg_soma_err),
        _color(result.overall_error_reduction),
    )
    summary_table.add_row(
        "Retries", _fmt_pct(avg_base_retry), _fmt_pct(avg_soma_retry),
        _color(result.overall_retry_reduction),
    )
    summary_table.add_row(
        "Tokens", f"{avg_base_tokens:,.0f}", f"{avg_soma_tokens:,.0f}",
        _color(result.overall_token_savings),
    )

    console.print()
    console.print(Panel(summary_table, title="SOMA Benchmark Results", border_style="bright_magenta"))

    # Per-scenario tables
    for scenario in result.scenarios:
        table = Table(
            title=f"{scenario.scenario_name}",
            show_header=True,
            header_style="bold cyan",
        )
        table.add_column("Metric", style="dim")
        table.add_column("Baseline", justify="right")
        table.add_column("SOMA", justify="right")
        table.add_column("Reduction", justify="right")

        s_err = _scenario_avg(scenario.soma_runs, "error_rate")
        b_err = _scenario_avg(scenario.baseline_runs, "error_rate")
        s_retry = _scenario_avg(scenario.soma_runs, "retry_rate")
        b_retry = _scenario_avg(scenario.baseline_runs, "retry_rate")
        s_tokens = _scenario_avg(scenario.soma_runs, "total_tokens")
        b_tokens = _scenario_avg(scenario.baseline_runs, "total_tokens")

        table.add_row(
            "Error Rate", _fmt_pct(b_err), _fmt_pct(s_err),
            _color(scenario.error_reduction),
        )
        table.add_row(
            "Retry Rate", _fmt_pct(b_retry), _fmt_pct(s_retry),
            _color(scenario.retry_reduction),
        )
        table.add_row(
            "Tokens", f"{b_tokens:,.0f}", f"{s_tokens:,.0f}",
            _color(scenario.token_savings),
        )

        # TP/FP row
        tp = sum(r.true_positives for r in scenario.soma_runs)
        fp = sum(r.false_positives for r in scenario.soma_runs)
        total_alerts = tp + fp
        if total_alerts > 0:
            precision = tp / total_alerts
            table.add_row(
                "Detection", f"{tp} TP / {fp} FP", "", _fmt_pct(precision),
            )

        console.print()
        console.print(table)

    console.print()
    console.print(f"[dim]{result.runs_per_scenario} runs per scenario | {result.timestamp}[/dim]")
    console.print()


def render_progress(console: object, scenario_name: str, run_number: int, total_runs: int) -> None:
    """Print a simple progress line for live output during benchmark."""
    from rich.console import Console as _Console
    if isinstance(console, _Console):
        console.print(f"  [cyan]{scenario_name}[/cyan] Run {run_number}/{total_runs}...")
    else:
        print(f"  {scenario_name} Run {run_number}/{total_runs}...")


# ------------------------------------------------------------------
# Helpers
# ------------------------------------------------------------------


def _scenario_avg(runs: list, attr: str) -> float:
    """Average a numeric attribute across benchmark runs."""
    if not runs:
        return 0.0
    return sum(getattr(r, attr) for r in runs) / len(runs)


def _avg_baseline_error_rate(result: BenchmarkResult) -> float:
    vals = [_scenario_avg(s.baseline_runs, "error_rate") for s in result.scenarios]
    return sum(vals) / len(vals) if vals else 0.0


def _avg_soma_error_rate(result: BenchmarkResult) -> float:
    vals = [_scenario_avg(s.soma_runs, "error_rate") for s in result.scenarios]
    return sum(vals) / len(vals) if vals else 0.0


def _avg_baseline_retry_rate(result: BenchmarkResult) -> float:
    vals = [_scenario_avg(s.baseline_runs, "retry_rate") for s in result.scenarios]
    return sum(vals) / len(vals) if vals else 0.0


def _avg_soma_retry_rate(result: BenchmarkResult) -> float:
    vals = [_scenario_avg(s.soma_runs, "retry_rate") for s in result.scenarios]
    return sum(vals) / len(vals) if vals else 0.0


def _avg_baseline_tokens(result: BenchmarkResult) -> float:
    vals = [_scenario_avg(s.baseline_runs, "total_tokens") for s in result.scenarios]
    return sum(vals) / len(vals) if vals else 0.0


def _avg_soma_tokens(result: BenchmarkResult) -> float:
    vals = [_scenario_avg(s.soma_runs, "total_tokens") for s in result.scenarios]
    return sum(vals) / len(vals) if vals else 0.0
