"""Tests for soma.benchmark.report — Markdown and Rich output."""

from __future__ import annotations

from soma.benchmark.metrics import (
    BenchmarkMetrics,
    BenchmarkResult,
    ScenarioResult,
)
from soma.benchmark.report import generate_markdown, render_terminal


# ------------------------------------------------------------------
# Helpers
# ------------------------------------------------------------------


def _make_metrics(
    error_rate: float = 0.10,
    retry_rate: float = 0.05,
    total_tokens: int = 5000,
    total_errors: int = 2,
    total_retries: int = 1,
    total_actions: int = 20,
    true_positives: int = 3,
    false_positives: int = 1,
) -> BenchmarkMetrics:
    return BenchmarkMetrics(
        total_errors=total_errors,
        error_rate=error_rate,
        total_retries=total_retries,
        retry_rate=retry_rate,
        total_tokens=total_tokens,
        duration_seconds=0.5,
        total_actions=total_actions,
        mode_transitions=[{"from": "OBSERVE", "to": "GUIDE", "at_action": 5, "pressure": 0.3}],
        false_positives=false_positives,
        true_positives=true_positives,
        per_action=[],
    )


def _make_result() -> BenchmarkResult:
    soma_runs = [_make_metrics(error_rate=0.05, retry_rate=0.02, total_tokens=4000)]
    baseline_runs = [_make_metrics(error_rate=0.15, retry_rate=0.10, total_tokens=6000)]

    scenario_a = ScenarioResult(
        scenario_name="degrading_session",
        description="Degrading session -- error ramp 0% to 60%",
        soma_runs=soma_runs,
        baseline_runs=baseline_runs,
        error_reduction=0.667,
        retry_reduction=0.800,
        token_savings=0.333,
    )
    scenario_b = ScenarioResult(
        scenario_name="healthy_session",
        description="Healthy session -- 50 actions, ~5% error",
        soma_runs=[_make_metrics(error_rate=0.04, retry_rate=0.01, total_tokens=5000)],
        baseline_runs=[_make_metrics(error_rate=0.05, retry_rate=0.02, total_tokens=5200)],
        error_reduction=0.200,
        retry_reduction=0.500,
        token_savings=0.038,
    )
    scenario_c = ScenarioResult(
        scenario_name="multi_agent_coordination",
        description="Multi-agent coordination -- 2 agents, 60 actions each",
        soma_runs=[_make_metrics(error_rate=0.06, retry_rate=0.03, total_tokens=4500)],
        baseline_runs=[_make_metrics(error_rate=0.12, retry_rate=0.08, total_tokens=5500)],
        error_reduction=0.500,
        retry_reduction=0.625,
        token_savings=0.182,
    )

    return BenchmarkResult(
        scenarios=[scenario_a, scenario_b, scenario_c],
        timestamp="2026-04-01T00:00:00+00:00",
        runs_per_scenario=5,
        overall_error_reduction=0.456,
        overall_retry_reduction=0.642,
        overall_token_savings=0.184,
    )


# ------------------------------------------------------------------
# Tests
# ------------------------------------------------------------------


def test_generate_markdown_contains_summary_table():
    md = generate_markdown(_make_result())
    assert "| Metric |" in md
    assert "| Without SOMA |" in md or "Without SOMA" in md


def test_generate_markdown_contains_scenario_sections():
    md = generate_markdown(_make_result())
    assert "### degrading_session" in md
    assert "### healthy_session" in md
    assert "### multi_agent_coordination" in md


def test_generate_markdown_footer():
    md = generate_markdown(_make_result())
    assert "Auto-generated by" in md
    assert "`soma benchmark`" in md


def test_generate_markdown_header():
    md = generate_markdown(_make_result())
    assert "# SOMA Benchmark Results" in md


def test_render_terminal_no_crash():
    """render_terminal should run without exceptions on synthetic data."""
    result = _make_result()
    render_terminal(result)  # no assertion — just must not crash


def test_markdown_has_real_numbers():
    """Verify actual percentages appear, not placeholders."""
    md = generate_markdown(_make_result())
    # Should contain percentage numbers
    assert "%" in md
    # Should not contain placeholder text
    assert "XX%" not in md
    assert "placeholder" not in md.lower()


def test_generate_markdown_detection_stats():
    """Verify true/false positive stats appear in markdown."""
    md = generate_markdown(_make_result())
    assert "true positive" in md.lower() or "TP" in md or "precision" in md.lower()
